<main>
    <p>This is an online version of the famous game stop.
    Rules are simple:</p>

    <h2>Host:</h2>
    <ol>
        <li>Create a list of topics (ex: Color, country, city, movie)</li>
        <li>send the url for the players to join</li>
        <li>control when new rounds start</li>
    </ol>

    <h2>Player:</h2>
    <ol>
        <li>Wait for a round to start</li>
        <li>Write one word that starts with the round letter for each topic</li>
        <li>Click finish when you are done</li>
        <li>Have fun!</li>
    </ol>

    <h2>Points:</h2>
    <ul>
        <li>2 points for every unique word</li>
        <li>1 point for every word that similar to another player</li>
        <li>-1 point for every invalid word (Host will be able to mark them in the end of the round)</li>
    </ul>

    <br><br>
